// teamsSideloadData is the template model shared by the pwsh and bash scripts.
type teamsSideloadData struct {
	AgentName     string
	BotName       string
	MsaAppID      string
	TeamsAppId    string
	ColorPngB64   string
	OutlinePngB64 string
}

// teamsSideloadScriptContent renders one pack-and-sideload script from the given
// template with the azd-controlled ids baked in.
func teamsSideloadScriptContent(
	tmpl *template.Template, agentName, botName, msaAppID string,
) string {
	var buf bytes.Buffer
	// Inputs are azd-controlled resource names/ids and the templates are
	// compile-time embedded, so execution cannot realistically fail.
	_ = tmpl.Execute(&buf, teamsSideloadData{
		AgentName:     agentName,
		BotName:       botName,
		MsaAppID:      msaAppID,
		TeamsAppId:    deterministicTeamsAppID(botName),
		ColorPngB64:   teamsColorIconB64,
		OutlinePngB64: teamsOutlineIconB64,
	})
	return buf.String()
}

// writeTeamsSideloadScripts writes runnable pwsh + bash pack-and-sideload scripts
// next to the agent source so the user can finish the last manual mile (package
// the Teams app + sideload it for themselves) in one command. It returns the
// paths written. Best-effort: any script that fails to write is skipped and
// logged, and this never blocks or fails the deploy. All ids are baked in from
// the deploy, so the generated scripts make no Azure calls.
func writeTeamsSideloadScripts(
	proj *azdext.ProjectConfig, svc *azdext.ServiceConfig, agentName, botName, msaAppID string,
) []string {
	scripts := []struct {
		file string
		tmpl *template.Template
		mode os.FileMode
	}{
		{teamsSideloadScriptPwsh, teamsSideloadPwshTmpl, 0o600},
		{teamsSideloadScriptBash, teamsSideloadBashTmpl, 0o700},
	}

	var written []string
	var created []string
	for _, s := range scripts {
		scriptPath, err := paths.JoinAllowRoot(proj.GetPath(), svc.GetRelativePath(), s.file)
		if err != nil {
			log.Printf("postdeploy: skipping Teams sideload script %q: %v", s.file, err)
			continue
		}
		content := teamsSideloadScriptContent(s.tmpl, agentName, botName, msaAppID)

		// Atomically claim/refresh the path keyed on the agent (service) name,
		// which is stable across redeploys AND azd environments (unlike the
		// version-scoped msaAppId or the subscription/RG-salted bot name): a
		// user-owned file, a different agent's script sharing this source
		// directory, or a non-regular file is left untouched, and concurrent
		// service deploys cannot clobber each other.
		ok, isNew, reason := writeOwnedGeneratedFile(scriptPath, content, s.mode, agentName, nil)
		if !ok {
			if reason != "" {
				log.Printf("postdeploy: Teams sideload script %q %s", scriptPath, reason)
			}
			continue
		}
		written = append(written, scriptPath)
		if isNew {
			created = append(created, scriptPath)
		}
	}

	// The .ps1 and .sh are claimed independently, so a rare race (e.g. a name
	// collision, or two different agents deploying concurrently from the same
	// source directory) can leave us with only one half of the pair -- and a lone
	// script carries a bot id its partner does not. Rather than advertise or leave
	// a split pair, roll back the file(s) THIS call freshly created (never a
	// pre-existing/refreshed one) and report nothing written. A redeploy then
	// regenerates the full pair once the collision is gone.
	if len(written) != len(scripts) {
		for _, p := range created {
			_ = os.Remove(p)
		}
		return nil
	}
	return written
}

// preferredSideloadScript returns the generated script that matches the current
// OS (the .ps1 on Windows, the .sh elsewhere), or "" if no matching script was
// written. It deliberately does not fall back to the other platform's script:
// running a .ps1 on Linux/macOS or a .sh on Windows would emit the wrong shell
// syntax, so the caller shows the guide/manual fallback instead.
func preferredSideloadScript(scriptPaths []string) string {
	wantPwsh := runtime.GOOS == "windows"
	for _, p := range scriptPaths {
		isPwsh := strings.HasSuffix(p, ".ps1")
		if isPwsh == wantPwsh {
			return p
		}
	}
	return ""
}

// sideloadRunCommand returns a runnable, expansion-safe invocation of the
// generated script for a user-facing hint. The .ps1 branch targets PowerShell
// (where a .ps1 is run and where azd's own output is typically shown) and uses
// powershell.exe, which ships with every Windows install (unlike PowerShell 7's
// pwsh) and takes an explicit -File path (not the PowerShell-only `&` call
// operator). It passes -ExecutionPolicy Bypass so the child script still runs on
// a default client whose policy is Restricted (the bypass is process-scoped and
// does not change the machine/user policy). The path is wrapped in SINGLE quotes
// so PowerShell treats it as a literal -- no `$name`, `$()`, or backtick
// expansion -- while backslashes stay intact so the Windows path resolves; an
// embedded single quote is escaped by doubling it (PowerShell literal-string
// rule). The .sh branch uses a POSIX single-quoted literal. See cli/azd/AGENTS.md
// ("Shell-safe output").
func sideloadRunCommand(scriptPath string) string {
	if strings.HasSuffix(scriptPath, ".ps1") {
		return `powershell -NoProfile -ExecutionPolicy Bypass -File '` + strings.ReplaceAll(scriptPath, "'", "''") + `'`
	}
	// POSIX single-quoted literal: close the quote, add an escaped ', reopen.
	return "bash '" + strings.ReplaceAll(scriptPath, "'", `'\''`) + "'"
}
