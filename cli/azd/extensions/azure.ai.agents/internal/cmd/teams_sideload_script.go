// Generated pack-and-sideload script file names. They are written next to the
// agent source alongside TEAMS_APP_SETUP.md and complete the last manual mile
// (build the Teams app zip + `atk install --scope Personal`) in one command.
const (
	teamsSideloadScriptPwsh = "pack-and-sideload-teams-app.ps1"
	teamsSideloadScriptBash = "pack-and-sideload-teams-app.sh"

	// teamsSideloadTargets is the number of pack+sideload scripts
	// writeTeamsSideloadScripts emits (one per supported shell). The guide and
	// next-steps output advertise all of them by name, so the fast path is only
	// offered when every target was written -- a partial write (e.g. one script
	// collided with a user-owned file) must not advertise a file azd did not
	// generate.
	teamsSideloadTargets = 2

	// teamsSideloadGeneratedMarker is the generic prefix that appears in the
	// header of every azd-generated pack+sideload script and setup guide. The
	// full marker also names the generating agent (see teamsGeneratedMarkerFor),
	// so writeTeamsSideloadScripts / writeTeamsSetupGuide only overwrite a file
	// they generated for the SAME agent: a user-owned file sharing the name, or a
	// file another activity service generated for a different agent (e.g. two
	// services that resolve to one source directory), is left untouched.
	teamsSideloadGeneratedMarker = "Generated by 'azd deploy' (activity protocol)"
)

// teamsGeneratedMarkerFor returns the agent-specific generated marker embedded in
// the scripts and guide azd writes for the given agent. The key is the agent
// (service) name: it is stable across redeploys, managed-identity recreation, AND
// azd environments -- unlike the version-scoped msaAppId/instance client id or the
// subscription/resource-group-salted bot name -- yet is distinct per service.
// Keying on it lets postdeploy recognize and refresh its own prior output,
// including when the same service is later deployed to a second environment, while
// leaving a different agent's output that shares the source directory untouched
// (must not be clobbered -- only the last writer's package would install). The
// trailing period delimits the name so one agent's marker is not a prefix of
// another's (e.g. "svc" must not match a file generated for "svc-2").
func teamsGeneratedMarkerFor(agentName string) string {
	return teamsSideloadGeneratedMarker + " for agent " + agentName + "."
}

// canWriteGeneratedFile reports whether path may be (over)written with a file
// azd generated for agentName. It is true when the path is absent or is a regular
// file already generated for the SAME agent (an idempotent refresh). It is
// false -- with a log-ready reason -- for a non-regular file (symlink, FIFO,
// device, directory), a user-owned file, or a file generated for a DIFFERENT
// agent, so none of those is silently clobbered.
func canWriteGeneratedFile(path, agentName string) (bool, string) {
	info, err := os.Lstat(path)
	if err != nil {
		// Absent, or the stat itself failed: attempt the write; WriteFile logs
		// any failure.
		return true, ""
	}
	if !info.Mode().IsRegular() {
		return false, "is not a regular file; leaving it untouched (rename it to get the generated file)"
	}
	existing, readErr := os.ReadFile(path)
	if readErr != nil {
		return false, "could not be read to verify its origin; leaving it untouched"
	}
	if strings.Contains(string(existing), teamsGeneratedMarkerFor(agentName)) {
		return true, ""
	}
	if strings.Contains(string(existing), teamsSideloadGeneratedMarker) {
		return false, "was generated for a different agent (another activity service shares this " +
			"source directory); leaving it untouched"
	}
	return false, "already exists and was not generated by azd; leaving it untouched " +
		"(rename it to get the generated file)"
}

// writeOwnedGeneratedFile writes content to path with mode, but only when azd may
// own the path. It atomically claims a brand-new path (O_CREATE|O_EXCL, so under
// a parallel service-deploy graph only one concurrent postdeploy wins the create
// and the losers fall through to the ownership check), and for an existing path
// it refreshes in place only when the file is a regular file this same agentName
// generated -- replacing it atomically via a temp file + rename so a reader never
// sees a partial file and a pre-existing symlink is replaced rather than followed.
// writeOwnedGeneratedFile returns (written, created, reason). written is true
// when the file now holds content; created is true only when this call
// exclusively created a brand-new file (false when it refreshed an existing one),
// which lets the caller roll back a half-written pair. reason explains why an
// untouched file was left ("" means the caller should stay silent).
func writeOwnedGeneratedFile(
	path, content string, mode os.FileMode, agentName string, isLegacy func(string) bool,
) (written bool, created bool, reason string) {
	// Fast path: atomically create a new file. O_EXCL guarantees exactly one
	// concurrent writer wins the create; everyone else gets ErrExist below.
	if f, err := os.OpenFile(path, os.O_CREATE|os.O_EXCL|os.O_WRONLY, mode); err == nil {
		_, writeErr := f.WriteString(content)
		closeErr := f.Close()
		if joined := errors.Join(writeErr, closeErr); joined != nil {
			// A partial/zero-length file left here would lack the ownership
			// marker and be treated as user-owned on every later deploy, so
			// remove the file we exclusively created before returning.
			_ = os.Remove(path)
			return false, false, fmt.Sprintf("could not be written: %v", joined)
		}
		// os.OpenFile applies mode subject to umask, so re-assert it (the bash
		// script must stay executable).
		if err := os.Chmod(path, mode); err != nil {
			return false, false, fmt.Sprintf("could not be made executable: %v", err)
		}
		return true, true, ""
	} else if !errors.Is(err, os.ErrExist) {
		return false, false, fmt.Sprintf("could not be created: %v", err)
	}

	// The path already exists: only replace it if we own it (same agent) or it is
	// a recognized legacy generated file. Never touch a user file, a different
	// agent's file, or a non-regular file.
	if ok, reason := canWriteGeneratedFile(path, agentName); !ok {
		if isLegacy == nil || !isLegacy(path) {
			return false, false, reason
		}
	}
	if err := replaceGeneratedFile(path, content, mode); err != nil {
		return false, false, fmt.Sprintf("could not be replaced: %v", err)
	}
	return true, false, ""
}

// replaceGeneratedFile stages content in a temp file in path's directory, then
// renames it over path. os.Rename replaces the destination in a single call
// (rename(2) on Unix; a replace-existing move on Windows). Go
// does not promise the replace is atomic on every platform, but because the full
// content is written to the temp file first, the destination is never left
// holding a partially written file, and a symlink at path is replaced rather
// than having its target truncated.
func replaceGeneratedFile(path, content string, mode os.FileMode) error {
	tmp, err := os.CreateTemp(filepath.Dir(path), ".azd-teams-*.tmp")
	if err != nil {
		return err
	}
	tmpName := tmp.Name()
	// Best-effort cleanup; a no-op once the rename below succeeds.
	defer func() { _ = os.Remove(tmpName) }()
	if _, err := tmp.WriteString(content); err != nil {
		_ = tmp.Close()
		return err
	}
	if err := tmp.Close(); err != nil {
		return err
	}
	if err := os.Chmod(tmpName, mode); err != nil {
		return err
	}
	return os.Rename(tmpName, path)
}

